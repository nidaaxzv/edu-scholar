    private void autonumber() {
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "select * from data_jurnal order by no_jurnal desc";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String noJurnal = r.getString("no_jurnal").substring(2);
                String no = "" + (Integer.parseInt(noJurnal) + 1);
                String zero = "";

                if (no.length() == 1) {
                    zero = "00";
                } else if (no.length() == 2) {
                    zero = "0";
                } else if (no.length() == 3) {
                    zero = "";
                }
                txt_noJurnal.setText("J" + zero + no);
            } else {
                txt_noJurnal.setText("J003");
            }
            r.close();
            s.close();

        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }

