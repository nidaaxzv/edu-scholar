    private void btn_subActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_subActionPerformed
        if (txt_prg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Teks Pengarang harap diisi.");
        } else if (txt_jdl.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Teks Judul harap diisi.");
        } else if (txt_thn.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Teks Tahun harap diisi.");
        } else if (txt_pnerbit.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Teks Penerbit harap diisi.");
        } else if (txt_topik.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Teks Topik harap diisi.");
        } else if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Teks User Id harap diisi.");
        } else {
        try {
            String sql = "insert into data_jurnal values ('"+txt_noJurnal.getText()+"','"+txt_prg.getText()+"','"+txt_jdl.getText()+"','"+txt_thn.getText()+"','"+txt_pnerbit.getText()+"','"+txt_topik.getText()+"','"+txtId.getText()+"')";
            java.sql.Connection c = Koneksi.getKoneksi();
            PreparedStatement ps = c.prepareStatement(sql);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Jurnal berhasil disubmit!");
            clearForm();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        }
    }//GEN-LAST:event_btn_subActionPerformed

