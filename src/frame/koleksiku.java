    private void Fetch() {
        DefaultTableModel df = new DefaultTableModel();
        df.addColumn("Nomor");
        df.addColumn("No jurnal");
        df.addColumn("Pengarang");
        df.addColumn("Judul");
        df.addColumn("Tahun");
        df.addColumn("Penerbit");
        df.addColumn("Topik");
        
        try {
            int no = 1;
            Connection conn = Koneksi.getKoneksi();
            String sql = "SELECT * FROM koleksiku WHERE user ='"+namaTxt.getText()+"' ORDER BY no_jurnal ASC;";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                df.addRow(new Object[]{no++, rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)});
            }
            tableKol.setModel(df);
        } catch (SQLException ex) {
            Logger.getLogger(koleksiku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void loadTopik() {
        try {
            Connection conn = Koneksi.getKoneksi();
            String sql = "SELECT topik FROM koleksiku WHERE user ='"+namaTxt.getText()+"';";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery(sql);
            topikCmb.removeAllItems();
            while(rs.next()) {
                topikCmb.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(koleksiku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

