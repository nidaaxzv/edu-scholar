    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        try {
            Connection conn = Koneksi.getKoneksi();
            int jawab = JOptionPane.showOptionDialog(this, "Anda yakin akan menghapus data ini?", "Delete", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if (jawab == JOptionPane.OK_OPTION) {
            String sql = "DELETE FROM koleksiku WHERE no_jurnal ='"+noJurnal.getText()+"'";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            }
            Fetch();
        } catch (SQLException ex) {
            Logger.getLogger(koleksiku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteMouseClicked

    private void cariMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cariMouseClicked
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nomor");
        model.addColumn("No Jurnal");
        model.addColumn("Pengarang");
        model.addColumn("Judul");
        model.addColumn("Tahun");
        model.addColumn("Penerbit");
        model.addColumn("Topik");

        try {
            Connection conn = Koneksi.getKoneksi();
            String topik = topikCmb.getSelectedItem().toString();
            String sql = "SELECT * FROM koleksiku WHERE topik=? AND user ='"+namaTxt.getText()+"';";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, topik);
            ResultSet rs = ps.executeQuery();
            int no = 1;
            while (rs.next()) {
                model.addRow(new Object[]{
                    no++,
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                });
            }
            tableKol.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(koleksiku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cariMouseClicked

