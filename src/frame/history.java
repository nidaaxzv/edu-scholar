    private void Fetch1() {
        int q;
        try {
            Connection conn = Koneksi.getKoneksi();
            String sql = "SELECT * FROM data_jurnal WHERE user_id ='"+idTxt.getText()+"';";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData res = rs.getMetaData();
            q = res.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) table2.getModel();
            df.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int a = 1; a <= q; a++) {
                    v2.add(rs.getString("no_jurnal"));
                    v2.add(rs.getString("pengarang"));
                    v2.add(rs.getString("judul"));
                    v2.add(rs.getString("tahun"));
                    v2.add(rs.getString("penerbit"));
                    v2.add(rs.getString("topik"));
                    v2.add(rs.getString("user_id"));
                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(history.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void LoadNoJurnal() {

        try {
            Connection conn = Koneksi.getKoneksi();
            String sql = "SELECT no_jurnal FROM `data_jurnal` WHERE user_id ='"+idTxt.getText()+"';";
            PreparedStatement s = conn.prepareStatement(sql);
            ResultSet rs = s.executeQuery();
            boxNo.removeAllItems();
            while (rs.next()) {
                boxNo.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(history.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

