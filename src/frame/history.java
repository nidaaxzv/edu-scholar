    private void BtnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDelActionPerformed
        // TODO add your handling code here:
        int question = JOptionPane.showConfirmDialog(null, "Yakin akan menghapus data jurnal ?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (question == JOptionPane.OK_OPTION) {
            try {
                Connection conn = Koneksi.getKoneksi();
                String noJur = boxNo.getSelectedItem().toString();
                String sql = "DELETE FROM data_jurnal WHERE no_jurnal=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, noJur);

                int k = ps.executeUpdate();
                if (k == 1) {
                    JOptionPane.showMessageDialog(this, "Data telah dihapus");
                    clearForm();
                    boxNo.requestFocus();
                    LoadNoJurnal();
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal dihapus");

                }
            } catch (SQLException ex) {
                Logger.getLogger(history.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (question == JOptionPane.CANCEL_OPTION) {

        }
    }//GEN-LAST:event_BtnDelActionPerformed

    private void BtnUptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUptActionPerformed
        try {
            // TODO add your handling code here:
            Connection c = Koneksi.getKoneksi();
            String prg = txtPg.getText();
            String jdl = txtJud.getText();
            String th = txtThn.getText();
            String pb = txtPnb.getText();
            String top = txtTp.getText();
            String noJur = boxNo.getSelectedItem().toString();
            String sql = "UPDATE data_jurnal SET pengarang=?,judul=?,tahun=?,penerbit=?,topik=? WHERE no_jurnal=?";
            PreparedStatement ps = c.prepareStatement(sql);

            ps.setString(1, prg);
            ps.setString(2, jdl);
            ps.setString(3, th);
            ps.setString(4, pb);
            ps.setString(5, top);
            ps.setString(6, noJur);

            int k = ps.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Data telah berhasil diupdate!");
                clearForm();
                Fetch1();
                boxNo.requestFocus();
                LoadNoJurnal();
            }
        } catch (SQLException ex) {
            Logger.getLogger(history.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_BtnUptActionPerformed

